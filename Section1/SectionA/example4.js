// Compound Assignment Operators

/* Combining operators with the assignment operators allows you to take the
value of a variable, do something to it and then return the new value
back to the variable */

let a = 5;
console.log("a+=6", a+=6); // output -> 11;
console.log("a-=6", a-=6); // output -> 5;
console.log("a*=6", a*=6); // output -> 30;
console.log("a/=6", a/=6); // output -> 5;
console.log("a%=2", a%=2); // output -> 1;

let b = 3;
console.log("b**=2", b**=2); // output -> 9;